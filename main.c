void Send_AT_Command(char* command)
{
    sprintf(TxBuffer, "%s\r\n", command);
    HAL_UART_Transmit(&huart1, (uint8_t*)TxBuffer, strlen(TxBuffer), 1000);
}

int main(void)
{
    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();
    MX_USART1_UART_Init();
    
    HAL_UART_Receive_IT(&huart1, RxBuffer, 1);

    while(1)
    {
        if(HAL_GetTick() - lastTick >= 5000) // Tang delay lên 5s
        {
            lastTick = HAL_GetTick();
            
            switch(cmd_index)
{
    case 0:
        Send_AT_Command("AT+CPIN");
        HAL_Delay(1000);
        cmd_index++;
        break;
    case 1:
        Send_AT_Command("AT+NETOPEN");
        HAL_Delay(3000);
        cmd_index++;
        break;
    case 2:
        Send_AT_Command("AT+CIPOPEN=1,\"TCP\",\"0.tcp.ap.ngrok.io\",\"10994\"");
        HAL_Delay(3000);
        cmd_index++;
        break;
    case 3:
        Send_AT_Command("AT+CIPSEND=1,16"); 
        HAL_Delay(1000);     
        cmd_index++;
        break;
    case 4:
        HAL_UART_Transmit(&huart1, (uint8_t*)"Hello from STM32", 16, 1000);  // G?i data riêng
        HAL_Delay(2000);
        cmd_index++;
        break;
    default:
        cmd_index = 0;
        HAL_Delay(2000);
        break;
}
        }
    }
}

